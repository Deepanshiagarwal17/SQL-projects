select * from healthcare;

-- RETRIEVE THE NAMES OF ALL PATIENTS IN THE DATABASE--
select name from healthcare;

-- LIST THE UNIQUE MEDICAL CONDITION PRESENT IN THE DATABASE--
select distinct(medical_condition) from healthcare;

-- FIND THE TOTAL NUMBER OF DOCTOR IN THE HEALTHCARE SYSTEM--
select count(doctor) from healthcare;

-- RETRIEVE THE DETAILS OF ALL FEMALE PATIENTS--
select * from healthcare where gender = 'female';

-- WHAT IS THE AVERAGE AGE OF PATIENTS IN THE DATABASE--
select avg(age) from healthcare group by NAME;

-- IDENTIFY THE PATIENT WHO HAVE MORE THAN ONE MEDICAL CONDITION--
select count(medical_condition) from healthcare group by medical_condition having count(medical_condition)>1;

-- LIST THE NAME OF ALL DOCTORS--
select doctor from healthcare;


-- FIND THE PATIENTS WHO ARE OLDER THN 60 YEARS AND HAVE DIABETES--
select * from healthcare where age >60 and medical_condition = 'diabetes';


-- IDENTIFY THE MOST COMMON MEDICAL CONDITION AMONG PATIENT--
select medical_condition,count(medical_condition)from healthcare group by medical_condition having count(medical_condition)>1
 order by medical_condition desc limit 1;

-- CALCULATED THE TOTAL REVENUE GENERATED BY THE HEALTHCARE SYSTEM--
SELECT SUM(BILLING_AMOUNT) FROM HEALTHCARE;


-- FIND THE DOCTOR WITH THE OLDEST PATIENT--
SELECT doctor, MAX(AGE) FROM HEALTHCARE group by doctor order by max(age) desc limit 1;

-- CALCULATE THE AVERAGE DURATION OF MEDICAL VISIT--
SELECT AVG(DURATION) FROM HEALTHCARE;

-- GIVE THE NAMES OF PATIENTS WHOSE NAME START WITH A --
SELECT * FROM HEALTHCARE WHERE NAME LIKE 'A%';

-- HOW MANY MALE AND FEMALE PATIENTS ARE THERE IN DATASET--
SELECT GENDER,COUNT(GENDER) FROM HEALTHCARE GROUP BY GENDER;

-- HOW MANY PATIENTS ARE NORMAL--
SELECT TEST_RESULTS, COUNT(TEST_RESULTS) FROM HEALTHCARE WHERE TEST_RESULTS = 'NORMAL';

-- HOW MANY APTIRNTS ARE ABNORMAL AND INCOLCLUSIVE--
SELECT TEST_RESULTS,COUNT(TEST_RESULTS) FROM HEALTHCARE WHERE TEST_RESULTS ='ABNORMAL' OR TEST_RESULTS= 'INCONCLUSIVE' GROUP BY TEST_RESULTS;

-- HOW MANY PATIENTS OF BLOOD GROUP AB+ ARE THERE OF AGE ABOVE 52--
SELECT BLOOD_TYPE,COUNT(BLOOD_TYPE) FROM HEALTHCARE WHERE AGE>52 AND BLOOD_TYPE = 'AB+' GROUP BY BLOOD_TYPE;

-- HOW MANY PATIENTS HAVE PAID THE BILL ABOVE THE 24890--
SELECT  COUNT(BILLING_AMOUNT) FROM HEALTHCARE WHERE BILLING_AMOUNT>24890;

-- WHAT ARE THE NAME OF PATIENTS HAVING ASTHMA--
SELECT NAME,MEDICAL_CONDITION FROM HEALTHCARE WHERE MEDICAL_CONDITION = 'ASTHMA';

-- HOW MANY PATIENTS HAVE CANCER--
SELECT MEDICAL_CONDITION, COUNT(MEDICAL_CONDITION) FROM HEALTHCARE WHERE MEDICAL_CONDITION ='CANCER' GROUP BY MEDICAL_CONDITION;

-- WHAT ARE THE NAMES AND BLOOD GROUPS OF PATIENTS ADMITTED TO ROOM NO 292--
SELECT NAME,BLOOD_TYPE,ROOM_NUMBER FROM HEALTHCARE WHERE ROOM_NUMBER = 292;

-- HOW MANY MALES HAVE ARTHRITIS--
SELECT MEDICAL_CONDITION, COUNT(MEDICAL_CONDITION)FROM HEALTHCARE WHERE GENDER = ' MALE' OR  MEDICAL_CONDITION = 'ARTHRITIS' GROUP BY MEDICAL_CONDITION;

-- HOW MANY PATIENTS WERE ADMITTED TO HARRIS  AND SONS HOSPITAL--
SELECT COUNT(NAME) FROM HEALTHCARE WHERE HOSPITAL ='HARRIS AND SONS';

-- WHAT ARE THE NAMES OF PATIENTS WHOSE INSURANCE PROVIDER  ARE CIGNA--
SELECT NAME,INSURANCE_PROVIDER FROM HEALTHCARE WHERE INSURANCE_PROVIDER ='CIGNA';

-- NAMES THE MEDICAL CONDITION HAVING PATIENTS WHOSE AGE IS GREATER THEN AVERAGE AGE --
SELECT NAME, AGE,MEDICAL_CONDITION FROM HEALTHCARE WHERE AGE>(SELECT AVG(AGE) FROM HEALTHCARE);






